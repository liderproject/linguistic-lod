                    <h3>Background</h3>    

                    <p>The Linguistic Linked Open Data cloud is a collaborative effort pursued by several members of the OWLG, with the general goal to develop a Linked Open Data (sub-)cloud of linguistic resources. The diagram is inspired by the <a href="http://lod-cloud.net">Linking Open Data cloud diagram</a> by Richard Cyganiak and Anja Jentzsch, and the resources included are chosen according to the same criteria of openness, availability and interlinking. Although not all resources are already available, we actively work towards this goal, and subsequent versions of this diagram will be restricted to openly available resources. Until that point, please refer to the diagram explicitly as a "draft"</p>

