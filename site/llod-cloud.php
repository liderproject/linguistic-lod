                        <h3>How to contribute</h3>

<p>The current version of the diagram was developed by John P. McCrae, Luca Matteis, Christian  Chiarcos, Sebastian Hellmann and Sebastian Nordhoff, however,  it is  officially attributed to the <b>Open Linguistics Working Group</b> and any working group member can modify it.</p>

<p>In order to add a new data set to the diagram draft, please follow this procedure:</p>

<ul class="square">
<li>Publish your resource as linked data</li>
<li>Create an entry for the resource on DataHub or LingHub. <a href="http://wiki.okfn.org/Working_Groups/Linguistics/How_to_contribute">Full instructions can be found here</a>.</li>
<li>Announce the resource <a href="http://lists.okfn.org/mailman/listinfo/open-linguistics"> on the mailing list</a>.</li>
</ul>

