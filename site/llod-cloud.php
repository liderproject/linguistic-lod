                        <h3>Joint activities</h3>

<p>The development of the diagram itself is designed as a joint activity. However, we all do pursue different application scenarios, so that concrete collaborations will normally not involve all contributors to the LLOD cloud, but rather focus on selected resources from the LLOD cloud and how their information can be combined. You can help the community by using the diagram above or extensions of it, on your websites and in your publications, and by mentioning the OWLG. If you need help to devise a description of the OWLG as a whole, you can ask the <a href="mailto:open-linguistics-owner@lists.okfn.org">mailing list administrators</a> to provide you with text snippets.</p>

<p>From time to time, we plan to present summaries of the progress of the OWLG and the LLOD cloud at selected conferences, in journals, etc. We would like this to be written on a really collaborative basis, and any contributor should feel invited to act as a co-author. Normally, we will announce plans for such summary publications by email to the people who committed to provide their data as part of the LLOD cloud.</p>

<p>So far, we have written a few papers that describe the vision behind the LLOD, that motivate the LLOD, discuss representative types of linguistic resources involved for selected examples, and that describe its development until October 2011 (i.e., before the community efforts really began). These are published as a separate part in the companion volume of the LDL workshop, and a concise summarization over these, extended with a focus on resources relevant to the Francophonie, has been submitted to a special issue of the French NLP journal TAL. With more and more people getting involved, and more resources becoming actually available, these publications should be quickly superseded by updated papers written by a larger number of people.</p>

